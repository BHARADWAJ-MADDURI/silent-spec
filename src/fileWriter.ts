import * as vscode from 'vscode';
import * as path from 'path';
import * as fs from 'fs';

// Phase 9 — dynamic header with date instead of static string
function buildHeader(): string {
  const date = new Date().toISOString().split('T')[0];
  return `// @auto-generated by SilentSpec | ${date}`;
}

const SS_START        = '// <SS-GENERATED-START>';
const SS_END          = '// <SS-GENERATED-END>';
const SS_USER_START   = '// <SS-USER-TESTS>';
const SS_USER_CONTENT = '// Add your own tests below — SilentSpec will never modify this section';
const SS_USER_END     = '// </SS-USER-TESTS>';

// Convention detection

async function detectNamingConvention(dir: string): Promise<'.spec.ts' | '.test.ts'> {
  try {
    const files = fs.readdirSync(dir);
    if (files.some(f => f.endsWith('.test.ts') || f.endsWith('.test.tsx'))) { return '.test.ts'; }
    if (files.some(f => f.endsWith('.spec.ts') || f.endsWith('.spec.tsx'))) { return '.spec.ts'; }
  } catch { /* fall through */ }

  const testFiles = await vscode.workspace.findFiles('**/*.test.ts', '**/node_modules/**', 1);
  const specFiles = await vscode.workspace.findFiles('**/*.spec.ts', '**/node_modules/**', 1);

  if (testFiles.length > 0) { return '.test.ts'; }
  if (specFiles.length > 0) { return '.spec.ts'; }

  return '.spec.ts';
}

export async function resolveSpecPath(sourcePath: string): Promise<string> {
  const ext  = path.extname(sourcePath);
  const base = path.basename(sourcePath, ext);
  const dir  = path.dirname(sourcePath);

  const convention = await detectNamingConvention(dir);

  const specExt = ext === '.tsx' ? '.spec.tsx'
                : ext === '.jsx' ? '.spec.jsx'
                : convention;

  const specName = `${base}${specExt}`;
  const testDirs = ['__tests__', 'tests', 'test'];

  for (const testDir of testDirs) {
    const potentialPath = path.join(dir, testDir);
    if (fs.existsSync(potentialPath) &&
        fs.lstatSync(potentialPath).isDirectory()) {
      return path.join(potentialPath, specName);
    }
  }

  const workspaceRoot = vscode.workspace.workspaceFolders?.[0]?.uri.fsPath;
  if (workspaceRoot && workspaceRoot !== dir) {
    for (const testDir of testDirs) {
      const rootTestPath = path.join(workspaceRoot, testDir);
      if (fs.existsSync(rootTestPath) &&
          fs.lstatSync(rootTestPath).isDirectory()) {
        const relativePath = path.relative(workspaceRoot, dir);
        const mirroredPath = path.join(rootTestPath, relativePath);
        return path.join(mirroredPath, specName);
      }
    }
  }

  return path.join(dir, specName);
}

// Content builders

function extractInner(validated: string): string {
  if (!validated.includes(SS_START)) { return validated.trim(); }
  return validated.split(SS_START)[1]?.split(SS_END)[0]?.trim() ?? '';
}

function buildNewFileContent(validated: string): string {
  const inner = extractInner(validated);
  return [
    buildHeader(),      // Phase 9 — dynamic date header
    SS_START,
    inner,
    SS_END,
    SS_USER_START,
    SS_USER_CONTENT,
    SS_USER_END,
  ].join('\n');
}

function buildUpdatedFileContent(
  existing: string,
  validated: string,
  log: (msg: string) => void
): string {
  const newInner = extractInner(validated);

  if (!newInner) {
    log('Warning: extracted inner content is empty — preserving existing spec file unchanged');
    return existing;
  }

  const hasMarkers = existing.includes(SS_START) && existing.includes(SS_END);

  let result: string;

  if (!hasMarkers) {
    result = [
      buildHeader(),    // Phase 9 — dynamic date header
      SS_START,
      newInner,
      SS_END,
      existing.trim(),
    ].join('\n');
  } else {
    const before = existing.split(SS_START)[0].trimEnd();
    const after  = existing.split(SS_END)[1] ?? '';

    result = [
      before.length > 0 ? before + '\n' : '',
      SS_START + '\n',
      newInner + '\n',
      SS_END,
      after,
    ].join('');
  }

  if (!result.includes(SS_USER_START)) {
    log('Notice: SS-USER-TESTS section missing — restoring');
    result = result.trimEnd() + '\n' + [
      SS_USER_START,
      SS_USER_CONTENT,
      SS_USER_END,
    ].join('\n');
  }

  return result;
}

// Internal writer — accepts already-resolved specPath

async function writeSpecFileToPath(
  specPath: string,
  validated: string,
  log: (msg: string) => void
): Promise<void> {
  const specUri = vscode.Uri.file(specPath);
  const isNew   = !fs.existsSync(specPath);

  const finalContent = isNew
    ? buildNewFileContent(validated)
    : buildUpdatedFileContent(fs.readFileSync(specPath, 'utf8'), validated, log);

  try {
    const edit = new vscode.WorkspaceEdit();

    if (isNew) {
      fs.mkdirSync(path.dirname(specPath), { recursive: true });

      edit.createFile(specUri, {
        ignoreIfExists: true,
        contents: Buffer.from(finalContent),
      });
      await vscode.workspace.applyEdit(edit);

      const doc = await vscode.workspace.openTextDocument(specUri);
      await doc.save();

      log(`Created: ${path.basename(specPath)} — SS-GENERATED block written, SS-USER-TESTS section ready`);

      const shouldOpen = vscode.workspace
        .getConfiguration('silentspec')
        .get<boolean>('openSpecOnCreate', true);

      if (shouldOpen) {
        await vscode.window.showTextDocument(doc, {
          viewColumn: vscode.ViewColumn.Beside,
          preserveFocus: true,
        });
      }

    } else {
      const doc = await vscode.workspace.openTextDocument(specUri);
      const fullRange = new vscode.Range(
        doc.positionAt(0),
        doc.positionAt(doc.getText().length)
      );
      edit.replace(specUri, fullRange, finalContent);
      await vscode.workspace.applyEdit(edit);
      await doc.save();

      log(`Updated: ${path.basename(specPath)} — SS-GENERATED block regenerated, SS-USER-TESTS preserved`);
    }

  } catch (error: unknown) {
    const msg = error instanceof Error ? error.message : String(error);

    // Phase 9 — surface permission errors as user-visible toast
    if (msg.includes('EACCES') || msg.includes('permission')) {
      void vscode.window.showErrorMessage(
        'SilentSpec: Cannot write spec file — check folder permissions.'
      );
    }

    log(`Error: file writer failed for ${path.basename(specPath)} — ${msg}`);
  }
}

// Public API — resolves specPath then delegates to writeSpecFileToPath

export async function writeSpecFile(
  sourcePath: string,
  validated: string,
  log: (msg: string) => void
): Promise<void> {
  const specPath = await resolveSpecPath(sourcePath);
  await writeSpecFileToPath(specPath, validated, log);
}

// Gap Finder merge writer — appends gap tests to existing SS-GENERATED block

export async function mergeSpecFile(
  sourcePath: string,
  newContent: string,
  log: (msg: string) => void
): Promise<void> {
  const specPath = await resolveSpecPath(sourcePath);

  const strippedNew = newContent
    .replace(/^\/\/ <SS-GENERATED-START>\s*/m, '')
    .replace(/\/\/ <SS-GENERATED-END>\s*$/m, '')
    .trim();

  if (!strippedNew) {
    log('Warning: gap generation returned empty content — spec file unchanged');
    return;
  }

  let existingGenerated = '';

  if (fs.existsSync(specPath)) {
    try {
      const specBytes = await vscode.workspace.fs.readFile(
        vscode.Uri.file(specPath)
      );
      const specContent = Buffer.from(specBytes).toString('utf8');

      // Phase 9 — git conflict marker check
      // Prevents corrupting spec files that have unresolved merge conflicts
      if (specContent.includes('<<<<<<<')) {
        log('Warning: Git conflict markers detected in spec file — skipping merge to prevent corruption');
        void vscode.window.showWarningMessage(
          'SilentSpec: Git conflict markers found in spec file. Resolve conflicts before auto-merging.'
        );
        return;
      }

      const startIdx = specContent.indexOf(SS_START);
      const endIdx   = specContent.indexOf(SS_END);

      if (startIdx !== -1 && endIdx !== -1) {
        existingGenerated = specContent
          .slice(startIdx + SS_START.length, endIdx)
          .trim();
        log('Gap merge: extracted existing SS-GENERATED block');
      }
    } catch {
      log('Gap merge: could not read existing spec — writing fresh');
    }
  }

  const combined = existingGenerated
    ? `${existingGenerated}\n\n${strippedNew}`
    : strippedNew;

  const merged = [
    SS_START,
    combined,
    SS_END,
  ].join('\n');

  await writeSpecFileToPath(specPath, merged, log);
  log('Gap merge: spec file updated — gap tests appended to SS-GENERATED block');
}